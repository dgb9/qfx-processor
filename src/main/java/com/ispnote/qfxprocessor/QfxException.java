package com.ispnote.qfxprocessor;

public class QfxException extends Exception {

	private static final long serialVersionUID = 7217264929325707045L;

	public QfxException() {
		super();
	}

	public QfxException(String message, Throwable cause) {
		super(message, cause);
	}

	public QfxException(String message) {
		super(message);
	}

	public QfxException(Throwable cause) {
		super(cause);
	}

}
